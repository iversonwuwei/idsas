package com.zdtx.ifms.specific.model.query;

public class Track {
	private String o_busname;
	private String o_date;
	private String o_time;
	private String o_longitude;
	private String o_latitude;
	private Long o_speed;
	private Long o_direction;
	
	public String getO_busname() {
		return o_busname;
	}
	public void setO_busname(String o_busname) {
		this.o_busname = o_busname;
	}
	public String getO_date() {
		return o_date;
	}
	public void setO_date(String o_date) {
		this.o_date = o_date;
	}
	public String getO_time() {
		return o_time;
	}
	public void setO_time(String o_time) {
		this.o_time = o_time;
	}
	public String getO_longitude() {
		return o_longitude;
	}
	public void setO_longitude(String o_longitude) {
		this.o_longitude = o_longitude;
	}
	public String getO_latitude() {
		return o_latitude;
	}
	public void setO_latitude(String o_latitude) {
		this.o_latitude = o_latitude;
	}
	public Long getO_speed() {
		return o_speed;
	}
	public void setO_speed(Long o_speed) {
		this.o_speed = o_speed;
	}
	public Long getO_direction() {
		return o_direction;
	}
	public void setO_direction(Long o_direction) {
		this.o_direction = o_direction;
	}
	
	

}
